const ROCK_1 = 'A', ROCK_2 = 'X',
      PAPER_1 = 'B', PAPER_2 = 'Y',
      SCISSOR_1 = 'C', SCISSOR_2 = 'Z',
      LOSE = 'X', DRAW = 'Y', WIN = 'Z';
let total = 0;

const calc_your_score_per_round = (opponent, you) => {
  // draw
  if ((opponent == ROCK_1 && you == ROCK_2) ||
      (opponent == PAPER_1 && you == PAPER_2) ||
      (opponent == SCISSOR_1 && you == SCISSOR_2)) {
    if (you == ROCK_2) {
      return DRAW_SCORE + ROCK_SCORE;
    }
    else if (you == PAPER_2) {
      return DRAW_SCORE + PAPER_SCORE;
    }
    else if (you == SCISSOR_2) {
      return DRAW_SCORE + SCISSOR_SCORE;
    }
  }
  // win
  else if (opponent == ROCK_1 && you == PAPER_2) {
    return WIN_SCORE + PAPER_SCORE;
  }
  else if (opponent == PAPER_1 && you == SCISSOR_2) {
    return WIN_SCORE + SCISSOR_SCORE;
  }
  else if (opponent == SCISSOR_1 && you == ROCK_2) {
    return WIN_SCORE + ROCK_SCORE;
  }
  // lose
  else if (you == ROCK_2) {
    return ROCK_SCORE;
  }
  else if (you == PAPER_2) {
    return PAPER_SCORE;
  }
  else if (you == SCISSOR_2) {
    return SCISSOR_SCORE;
  }
}

