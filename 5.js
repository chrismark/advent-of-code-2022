const process_moves2 = (move) => {
  move = move.replace(/move|from|to/g, '').trim().split('  ').map(m => parseInt(m));
  // if only one move do usual pop and push
  if (move[0] == 1) {
    actual_stacks[move[2]-1].push( actual_stacks[move[1]-1].pop() );
  }
  else { // if it's more than one, retain order as they appear before moving
    let temp = [];
    for (let i = 0; i < move[0]; i++) { // move move[0] times
      // pop from move[1] and push to temp
      temp.push( actual_stacks[move[1]-1].pop() );
    }
    // reverse order before pushing to move[2]
    temp.reverse();
    for (let i = 0; i < temp.length; i++) {
      actual_stacks[move[2]-1].push( temp[i] );
    }
  }
}

