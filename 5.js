const lines = input.split(/\n/g);
const indexEmpty = lines.indexOf('');
const stacks = lines.slice(0, indexEmpty-1);
const moves = lines.slice(indexEmpty+1);

const items = stacks.map(l => 
  // match [F] or '   ' (empty) stack item, they end up as an array
  l.match(/\s{0,1}(\[[A-Z]\]|\s{3})/g) 
  .map(i => 
    // remove [] and extra spaces (except if an empty item, they are retained as 3 spaces)
    i.replace(/^\s?(\[|\s)/, '').replace(/(\]|\s)$/, '') 
  )
);
const actual_stacks = [];
// loop starting from the bottom
for (let i = items.length - 1; i >= 0; i--) {
  // loop would be stack items from left to right
  for (let s = 0; s < items[i].length; s++) {
    // skip empty item
    if (items[i][s].trim().length == 0) { continue; } 

    if (i == items.length - 1) {
      // initialize each stack when at the bottom
      actual_stacks.push([ items[i][s] ]); 
    }
    else {
      // push to correct stack, from bottom upwards
      actual_stacks[s].push( items[i][s] );
    }
  }
}
