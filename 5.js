// console.log(moves);
// console.log('items: ', items);
console.log('actual_stacks: ', actual_stacks);

const process_moves = (move) => {
  // turn move 1 from 2 to 3 into [1,2,3]
  move = move.replace(/move|from|to/g, '').trim().split('  ').map(m => parseInt(m));

  // move move[0] times
  for (let i = 0; i < move[0]; i++) { 
    // pop from move[1] and push to move[2]
    actual_stacks[move[2]-1].push( actual_stacks[move[1]-1].pop() );
  }
}

